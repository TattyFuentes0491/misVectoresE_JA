    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(JP.misProductos.setAddProducto(jTextField1, jTextField2, jTextField3,i)){
            i++;//avanzamos
            JP.misProductos.setLlenarJTable(JTable1);
        }
        //validamos si puede registrar un nuevo producto por el tamaño del vector
        if(i == JP.misProductos.tam){
            jTextField1.setEnabled(false);
            jTextField2.setEnabled(false);
            jTextField3.setEnabled(false);
            jButton1.setEnabled(false);
            JOptionPane.showMessageDialog(null, 
                "El vector tiene todos los elementos registrados!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

